from main_laba4 import gen_bin_tree
import unittest


# Тесты
class TestMath(unittest.TestCase):
    def test_0(self):
        self.assertEqual(gen_bin_tree(0, 0), {0})

    def test_negative(self):
        self.assertEqual(gen_bin_tree(3, -10), {'-10': [{'-9': [{'-8': [{'-7': []}, {'64': []}]},
                 {'81': [{'82': []}, {'6561': []}]}]},
         {'100': [{'101': [{'102': []}, {'10201': []}]},
                  {'10000': [{'10001': []}, {'100000000': []}]}]}]})

    def test_str(self):
        self.assertEqual(gen_bin_tree('3', '13'), {'13': [{'14': [{'15': [{'16': []}, {'225': []}]},
                {'196': [{'197': []}, {'38416': []}]}]},
        {'169': [{'170': [{'171': []}, {'28900': []}]},
                 {'28561': [{'28562': []}, {'815730721': []}]}]}]})
    
    def test_bigInt(self):
        self.assertEqual(gen_bin_tree(3, 100000), {'100000': [{'100001': [{'100002': [{'100003': []}, {'10000400004': []}]},
                        {'10000200001': [{'10000200002': []},
                                         {'100004000060000400001': []}]}]},
            {'10000000000': [{'10000000001': [{'10000000002': []},  
                                              {'100000000020000000001': []}]},
                             {'100000000000000000000': [{'100000000000000000001': []},
                                                        {'10000000000000000000000000000000000000000': []}]}]}]})

   
if __name__ == '__main__':
    unittest.main()